# 题目要求：分别打印出以下集合的值 [cite: 86]

s1 = {32, 5,'c','32','11'}
s2 = set({32,'46',32,'aa'})
s3 = set('4,32,46,11,32')
s4 = set([1, 2, 3])              
s5 = set((1,2,3))
s6 = set({'a':1,'b':2,'c':3})

# --- 打印结果与说明 --- [cite: 86, 88]

print(f"s1 = {s1}")
# s1 = {32, 5, 'c', '32', '11'}
# (说明: 集合元素不重复，32 (int) 和 '32' (str) 是不同元素)

print(f"s2 = {s2}")
# s2 = {32, '46', 'aa'}
# (说明: 构造函数中的重复元素 32 被自动去除)

print(f"s3 = {s3}")
# s3 = {'4', '3', '2', '1', ',' '6'}
# (说明: set() 构造函数会迭代字符串中的 *每个字符* 作为元素，包括逗号)

print(f"s4 = {s4}")
# s4 = {1, 2, 3}
# (说明: 从列表创建集合)

print(f"s5 = {s5}")
# s5 = {1, 2, 3}
# (说明: 从元组创建集合)

print(f"s6 = {s6}")
# s6 = {'a', 'c', 'b'}
# (说明: 从字典创建集合时，默认只使用字典的 *键* )